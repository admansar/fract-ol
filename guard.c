#include "fractol.h"

int	guard(char *c)
{
	int	j;
	int	i;

	i = 0;
	j = 0;
	while (c[i])
	{
		if (c[i] == '.')
			j++;
		i++;
	}
	if (j > 1)
		return (1);
	i = 0;
	while (c[i])
	{
		if (!((c[i] >= '0' && c[i] <= '9') || c[i] == '.' || c[i] == '-'))
			return (1);
		i++;
	}
	return (0);
}
